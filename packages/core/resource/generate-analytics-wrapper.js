#!/usr/bin/env node
/*
 * Scans the analytics-schemas OpenAPI directory for object schemas whose
 * `eventName` property has a `default` value (i.e. the canonical event name)
 * and emits `src/analytics/events.ts` with:
 *   - one type alias per event (`AnalyticsEventXxx`) that narrows `eventName`
 *     to its literal value
 *   - the `AnalyticsEvent` discriminated union
 *
 * `openapi-typescript-codegen` types `eventName: string, default: <value>` as
 * plain `eventName: string`, so the discriminator is otherwise lost.
 *
 * Usage: generate-analytics-wrapper.js <schemas-openapi-dir> <out.ts>
 */
const fs = require('fs');
const path = require('path');
const yaml = require('js-yaml');

const [, , SCHEMAS_DIR, OUT_PATH] = process.argv;
if (!SCHEMAS_DIR || !OUT_PATH) {
    console.error(
        'usage: generate-analytics-wrapper.js <schemas-openapi-dir> <out.ts>'
    );
    process.exit(1);
}

const seen = new Map(); // eventName -> schemaName

for (const file of fs.readdirSync(SCHEMAS_DIR).sort()) {
    if (!file.endsWith('.yaml')) continue;
    if (file === 'analytics.yaml') continue; // top-level aliases, not events
    const doc = yaml.load(fs.readFileSync(path.join(SCHEMAS_DIR, file), 'utf8'));
    const schemas = doc?.components?.schemas;
    if (!schemas) continue;

    for (const [schemaName, schema] of Object.entries(schemas)) {
        const eventName = schema?.properties?.eventName?.default;
        if (typeof eventName !== 'string') continue;
        if (seen.has(eventName)) continue;
        seen.set(eventName, schemaName);
    }
}

if (seen.size === 0) {
    console.error('wrapper: no schemas with eventName.default were found');
    process.exit(1);
}

const events = [...seen.entries()]
    .map(([eventName, schemaName]) => ({ eventName, schemaName }))
    .sort((a, b) => a.schemaName.localeCompare(b.schemaName));

const aliasFor = schemaName =>
    `AnalyticsEvent${schemaName.replace(/Schema$/, '')}`;

const imports = events
    .map(
        ({ schemaName }) =>
            `import type { ${schemaName} } from './generated/models/${schemaName}';`
    )
    .join('\n');

const variants = events
    .map(
        ({ eventName, schemaName }) =>
            `export type ${aliasFor(schemaName)} = Omit<${schemaName}, 'eventName'> & {\n    eventName: '${eventName}';\n};`
    )
    .join('\n\n');

const union = `export type AnalyticsEvent =\n${events
    .map(({ schemaName }) => `    | ${aliasFor(schemaName)}`)
    .join('\n')};\n`;

const banner =
    '/* generated by resource/generate-analytics-wrapper.js -- do not edit */\n' +
    '/* eslint-disable */\n\n';

fs.writeFileSync(OUT_PATH, banner + imports + '\n\n' + variants + '\n\n' + union);
console.log(`wrapper: generated ${events.length} event types to ${OUT_PATH}`);
